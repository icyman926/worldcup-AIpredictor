function analyzeOdds(home, draw, away) {


















  const raw = { home: 1 / home, draw: 1 / draw, away: 1 / away };


















  const total = raw.home + raw.draw + raw.away;


















  return { margin: round((total - 1) * 100), normalized: { home: round((raw.home / total) * 100), draw: round((raw.draw / total) * 100), away: round((raw.away / total) * 100) } };


















}





































